class Place {
  int code;
  String name;
  String description;
  String activity;
  String city;
  String startDate;
  String endDate;
  double latitude;
  double longitude;
  int proCode;

  Place({
    required this.code,
    required this.name,
    required this.description,
    required this.activity,
    required this.city,
    required this.startDate,
    required this.endDate,
    required this.latitude,
    required this.longitude,
    required this.proCode,
  });
}
