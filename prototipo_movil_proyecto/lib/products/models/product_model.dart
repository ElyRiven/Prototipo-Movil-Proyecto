class Product {
  final int code;
  final String name;
  final String price;
  final String description;
  final String country;
  final DateTime startDate;
  final DateTime endDate;
  final String state;

  Product({
    required this.code,
    required this.name,
    required this.price,
    required this.description,
    required this.country,
    required this.startDate,
    required this.endDate,
    required this.state,
  });
}
